sec = int(input('Enter a time in a seconds: '))

def corr(time):
    if time < 10:
        time = '0' + str(time)
        return time
    return time

hours = sec // 3600
minutes = (sec - hours*3600) // 60
seconds = sec - hours*3600 - minutes*60

print(f"{corr(hours)}:{corr(minutes)}:{corr(seconds)}")

