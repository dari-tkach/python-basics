# Спортсмен занимается ежедневными пробежками. В первый день увеличивал результат на 10% относительно предыдущего. Требуется определить номер дня, на который результат спортсмена составит не менее b километров. Программа должна принимать значения параметров a и b и выводить одно натуральное число — номер дня.

def km(a, b):
    day = 1
    while a < b:
        print(f"{day}-й день: {a:.2f}")
        a += a*0.1
        day += 1
    print(f"{day}-й день: {a:.2f}")
    return day

a = int(input('Сколько км вы пробежали в первый день: '))
b = int(input('Ваша цель: '))
day = km(a, b)
print(f"Вы пробежите {b} км на {day} день")
